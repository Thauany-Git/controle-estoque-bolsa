    private void preencherComboVendedor() {
        try {
            List<Vendedor> lista = this.vendedorDAO.getVendedores();
            for (Vendedor v : lista) {
                cmbVendedor.addItem(v);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro ao preencher vendedores");
        }
    }

